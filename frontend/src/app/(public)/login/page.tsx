'use client';

import { useEffect } from 'react';
import { useRouter, useSearchParams } from 'next/navigation';
import { Suspense } from 'react';
import { getToken, getGoogleSignInUrl } from '@/lib/api';

function LoginContent() {
  const router = useRouter();
  const searchParams = useSearchParams();
  const redirect = searchParams.get('redirect') ?? '/members';

  useEffect(() => {
    if (getToken()) {
      router.replace(redirect);
    }
  }, [router, redirect]);

  const handleSignIn = () => {
    // ログイン後に元のページへ戻るため、callbackでredirectを使えるようにする
    // 現状のフローはGoogle → /auth/callback → /members なので、シンプルにGoogleへ
    window.location.href = getGoogleSignInUrl();
  };

  return (
    <div className="pt-20 min-h-screen flex items-center justify-center">
      <div className="w-full max-w-md px-6">
        <div className="card p-10 text-center">
          {/* Logo / Title */}
          <h1 className="text-2xl tracking-[0.3em] mb-2">MASASHI ENOKIDA</h1>
          <p className="text-taupe text-sm mb-10">会員ログイン</p>

          {/* Google Sign In Button */}
          <button
            onClick={handleSignIn}
            className="flex items-center justify-center gap-3 w-full border border-[#3d2020] rounded-lg px-4 py-3 text-beige hover:bg-burgundy transition-colors mb-6"
          >
            <svg width="20" height="20" viewBox="0 0 48 48" aria-hidden="true">
              <path
                fill="#EA4335"
                d="M24 9.5c3.54 0 6.71 1.22 9.21 3.6l6.85-6.85C35.9 2.38 30.47 0 24 0 14.62 0 6.51 5.38 2.56 13.22l7.98 6.19C12.43 13.72 17.74 9.5 24 9.5z"
              />
              <path
                fill="#4285F4"
                d="M46.98 24.55c0-1.57-.15-3.09-.38-4.55H24v9.02h12.94c-.58 2.96-2.26 5.48-4.78 7.18l7.73 6c4.51-4.18 7.09-10.36 7.09-17.65z"
              />
              <path
                fill="#FBBC05"
                d="M10.53 28.59c-.48-1.45-.76-2.99-.76-4.59s.27-3.14.76-4.59l-7.98-6.19C.92 16.46 0 20.12 0 24c0 3.88.92 7.54 2.56 10.78l7.97-6.19z"
              />
              <path
                fill="#34A853"
                d="M24 48c6.48 0 11.93-2.13 15.89-5.81l-7.73-6c-2.18 1.48-4.97 2.29-8.16 2.29-6.26 0-11.57-4.22-13.47-9.91l-7.98 6.19C6.51 42.62 14.62 48 24 48z"
              />
            </svg>
            Google アカウントでログイン
          </button>

          <p className="text-taupe text-xs leading-relaxed">
            Googleアカウントでログインすることで、
            <br />
            会員特典をご利用いただけます。
          </p>

          <div className="mt-8 pt-6 border-t border-[#3d2020]">
            <p className="text-taupe text-xs">
              まだ会員でない方は、ログインすると自動的に無料会員として登録されます。
            </p>
          </div>
        </div>
      </div>
    </div>
  );
}

export default function LoginPage() {
  return (
    <Suspense
      fallback={
        <div className="pt-20 min-h-screen flex items-center justify-center">
          <p className="text-taupe">読み込み中...</p>
        </div>
      }
    >
      <LoginContent />
    </Suspense>
  );
}
